public class Factori1 {
    public static void main(String[] args) {
        int FactorialFor = 9;
        int Factorial = 1;
        for (int i=1; i<=FactorialFor; i++)
        {
            Factorial = Factorial * i;
            System.out.println(Factorial);
        }
    }
}
